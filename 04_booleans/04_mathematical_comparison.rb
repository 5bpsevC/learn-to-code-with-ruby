# Operadores de comparación: < (menor que), <= (menor o igual que)
puts 1 < 9        # => true (1 es menor que 9)
puts 10 < 5       # => false (10 no es menor que 5)
puts 1 <= 5       # => true (1 es menor o igual que 5)
puts 5 <= 5       # => true (5 es igual a 5)
puts 8 <= 5       # => false (8 no es menor o igual que 5)

puts               # línea en blanco para separar secciones

# Operadores de comparación: > (mayor que), >= (mayor o igual que)
puts 10 > 5       # => true (10 es mayor que 5)
puts (-1 > -5)    # => true (-1 es mayor que -5)
puts 12 > 20      # => false (12 no es mayor que 20)
puts 8 >= 5       # => true (8 es mayor o igual que 5)
puts 5 >= 5       # => true (5 es igual a 5)
puts 3 >= 5       # => false (3 no es mayor o igual que 5)
