# Suma de números enteros
puts 4 + 3            # Salida: 7

# Concatenación de cadenas de texto
puts "race" + "car"   # Salida: racecar

# Concatenación de cadenas que parecen números
puts "4" + "3"        # Salida: 43

puts 4 + "3"   # ❌ Error
# TypeError: String can't be coerced into Integer
# Porque Ruby no convierte automáticamente entre tipos para operaciones mixtas.