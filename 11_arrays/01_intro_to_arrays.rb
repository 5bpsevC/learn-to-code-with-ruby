# Un array es una lista ordenada de objetos.
# Se puede guardar cualquier tipo de dato dentro de un array: números, strings, booleanos, etc.

# Creamos un array llamado 'numbers' que contiene varios números enteros
numbers = [4, 8, 15, 16, 23, 42]
p numbers  # Imprime el contenido del array en consola

# Creamos un array llamado 'things' con diferentes tipos de datos:
# un número entero, un valor booleano, un string y un número decimal (float)
things = [4, true, "Hello", 10.99]
p things  # También se imprime en consola

# Creamos un array llamado 'toys' con solo strings (juguetes)
toys = ["Teddy bear", "Water gun", "Board game"]
puts toys

# Creamos un array llamado 'registrations' que contiene valores booleanos
# Esto podría representar si las personas están registradas (true) o no (false)
registrations = [true, true, false, true, false]
p registrations  # Se imprime en consola
