# Imprime la longitud (cantidad de caracteres) de la cadena "hello world"
puts "hello world".length      # => 11

# Convierte la cadena "hello world" a mayúsculas
puts "hello world".upcase      # => "HELLO WORLD"

# Convierte la cadena "TOTALLY DIFFERENT" a minúsculas
puts "TOTALLY DIFFERENT".downcase   # => "totally different"

# Asigna la cadena a una variable llamada expression
expression = "TOTALLY DIFFERENT"

# Convierte el contenido de la variable expression a minúsculas y lo imprime
puts expression.downcase       # => "totally different"

puts "boris deddof".capitalize         # => "Boris Deddof"
puts "hELLO WORLD".capitalize   # => "Hello world"